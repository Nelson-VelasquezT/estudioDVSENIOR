# definición de listas

numeros = [1, 2, 3, 4, 5]

frutas = ["manzana", "plátano", "naranja"]

# acceso a elementos
print(numeros[0]) # 1
print(frutas[1]) # plátano

# modificación de elementos
frutas[0] = "uva"
print(frutas) # ["uva", "plátano", "naranja"]

# recorrer lista
[print(fruta) for fruta in frutas]




